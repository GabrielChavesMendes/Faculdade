int  Primo(int i){

int cont = 0;

for(int j = 1; j <= i; j++){
    if(i % j == 0){
        cont++;
    }
}
if(cont == 2){
    return 1;
}else{
    return 0;
}
}

int Inverter(int i){

int espelho = 0;

while (i != 0){
    int digito = i % 10;
    espelho = espelho * 10 + digito;
    i = i / 10;
}
return espelho;
}

