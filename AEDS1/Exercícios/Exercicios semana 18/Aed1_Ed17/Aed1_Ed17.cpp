void Ordenar_Fila(ref_intQueue fila){

    for (int i = 0; i < fila->length - 1; i++) {
        for (int j = 0; j < fila->length - 1 - i; j++) {
            if (fila->data[j] > fila->data[j + 1]) {
                int temp = fila->data[j];
                fila->data[j] = fila->data[j + 1];
                fila->data[j + 1] = temp;
            }
        }
    }
}

ref_intQueue Fila_Unida_Crescente(ref_intQueue fila1, int tam1, ref_intQueue fila2, int tam2){

    ref_intQueue fila = new intQueue;
    int tam_novo = tam1 + tam2;
    int *fila_nova = new int[tam_novo];

    Ordenar_Fila(fila1);
    Ordenar_Fila(fila2);

    int i = 0, j = 0, k = 0;
    while (i < tam1 && j < tam2) {
        if (fila1->data[i] <= fila2->data[j]) {
            fila_nova[k++] = fila1->data[i++];
        } else {
            fila_nova[k++] = fila2->data[j++];
        }
    }

    while (i < tam1){
        fila_nova[k++] = fila1->data[i++];
    }

    while (j < tam2){
        fila_nova[k++] = fila2->data[j++];
    }

    fila->data = fila_nova;
    fila->length = tam_novo;
    return fila;
}

