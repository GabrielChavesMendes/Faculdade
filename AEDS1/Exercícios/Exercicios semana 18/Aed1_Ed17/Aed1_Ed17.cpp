typedef struct s_intStack {
    int length;
    int *data;
} intStack;

typedef intStack* ref_intStack;

//------------------METODOS-----------------------//

ref_intStack Acrescentar_Topo(ref_intStack pilha, int valor){

    //ALOCA UM VETOR DINAMICAMENTE COM 1 TAMANHO A MAIS PARA ACRESCENTAR NO TOPO DA PILHA
    int *nova_pilha = new int[pilha->length + 1];

    //COPIAR OS DADOS DA PILHA ATUAL PARA O VETOR
    for (int i = 0; i < pilha->length; i++){
        nova_pilha[i] = pilha->data[i];
    }

    //ADICIONAR O NOVO VALOR AO TOPO DA PILHA NA PROXIMA POSICAO
    nova_pilha[pilha->length] = valor;

    delete[] pilha->data;

    //ATUALIZAR OS DADOS DA PILHA E DO TAMANHO
    pilha->data = nova_pilha;
    pilha->length++;

    return pilha;
}

