int OutroSimbolo(char frase) {

    if (!Maiuscula(frase) && !Minuscula(frase) && !Digito(frase) && !OperadorAritmetico(frase) && !OperadorLogico(frase) && !OperadorRelacional(frase)
        && !Separadores(frase)){
        return 1;
    } else {
        return 0;
    }
}

