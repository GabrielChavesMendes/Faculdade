int Fatorial2(int n) { // Função RECURSIVA para calcular o fatorial
    if (n == 0 ) {
        return 1;
    } else {
        return (n * Fatorial2(n - 1));
    }
}

int Fatorial3(int m) { // Função RECURSIVA para calcular o fatorial
    if (m == 0 ) {
        return 1;
    } else {
        return (m * Fatorial2(m - 1));
    }
}

