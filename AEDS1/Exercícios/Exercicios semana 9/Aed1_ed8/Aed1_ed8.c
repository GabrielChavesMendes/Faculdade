int DivisoresImpares(int n, int *vetor, int encontrados, int cont) {

int cont2 = 0;

while (cont < n){
    cont = cont + 1;
    if (n % cont == 0 && cont % 2 != 0){
        vetor[encontrados++] = cont;
        cont2++;
    }
}

if(encontrados < n){
    vetor = (int*)realloc(vetor, encontrados * sizeof(int));
}

return cont2;
}

