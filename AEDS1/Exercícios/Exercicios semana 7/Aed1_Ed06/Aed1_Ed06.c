int FatorialCalculo(int n){

if (n <= 1) {
    return 1;
}

return n * FatorialCalculo(n - 1);
}

int FatorialInverso(int n, int cont, double *soma, int numerador, int denominador) {

if (cont == n) {
    return 1;
}

numerador = numerador + 2;
denominador = denominador + 2;

printf("+ %d/%d! ", numerador, denominador);

*soma = *soma + (double)numerador / FatorialCalculo(denominador);

return FatorialInverso(n, cont + 1, soma, numerador, denominador);
}

