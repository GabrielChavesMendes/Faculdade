int Fibonacci(int n, int numero, int numero2, int soma, int cont, int *resultado){

if(cont == n){
    return 1;
}

if(numero % 2 == 0){
printf("%d ", numero);
*resultado = *resultado + numero;
cont++;

if (cont < n) {
    printf("+ ");
}
}

soma = numero + numero2;
numero = numero2;
numero2 = soma;

return Fibonacci(n, numero, numero2, soma, cont, resultado);
}

