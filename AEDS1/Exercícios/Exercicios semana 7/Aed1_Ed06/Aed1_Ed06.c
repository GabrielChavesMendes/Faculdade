int Potencia(int n, int x, int cont, int *soma, int potencia){

if(cont == n){
    return 1;
}

printf("+ %d^%d ", x, (potencia * 2) - 1);
*soma = *soma + pow(x, (potencia *2) - 1);

return Potencia(n, x, cont + 1, soma, potencia + 1);
}

