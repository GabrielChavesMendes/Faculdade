bool CriarArquivo(double **Matriz, int linhas, int colunas) {

FILE *arquivo;
arquivo = fopen("MATRIZ_EXTRA.TXT", "w");

if (arquivo == NULL) {
    printf("Erro ao criar o arquivo");
    return false;
}

for (int i = 0; i < linhas; i++) {
    for (int j = 0; j < colunas; j++) {
        fprintf(arquivo, "[%.0lf]\t", Matriz[i][j]);
    }
    fprintf(arquivo, "\n");
}

fclose(arquivo);
return true;

}

void Matriz_Quadrada(int x, int y){

int linhas = Linhas(x);
int colunas = Colunas(y);
int numero = 1;

if (linhas == colunas) {

    printf("E uma matriz quadrada\n");

    double **Matriz = (double **)malloc(linhas * sizeof(double *)); //alocacao dinamica

    for(int i = 0; i < linhas; i++){
        Matriz[i] = (double *)malloc(colunas * sizeof(double));
    }

    for(int i = 0; i < linhas; i++){
        for(int j = 0; j < colunas; j++){
            Matriz[i][j] = numero;
            numero++;
        }
    }

    for(int i = 0; i < linhas; i++){
        for(int j = 0; j < colunas; j++){
            printf("[%.0lf]\t", Matriz[i][j]);
        }
        printf("\n");
    }

    bool criado = CriarArquivo(Matriz, linhas, colunas);

    for(int i = 0; i < linhas; i++){
        free(Matriz[i]);
    }

    free(Matriz);

    if(criado){
        printf("Arquivo criado\n");
    }else{
        printf("Arquivo não criado\n");
    }
} else {
    printf("Não é uma matriz quadrada\n");
}
}

